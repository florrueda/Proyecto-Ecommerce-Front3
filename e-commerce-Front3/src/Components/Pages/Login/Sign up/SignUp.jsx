import * as React from "react";
import Button from "@mui/material/Button";
import CssBaseline from "@mui/material/CssBaseline";
import TextField from "@mui/material/TextField";
import Grid from "@mui/material/Grid";
import Box from "@mui/material/Box";
import Typography from "@mui/material/Typography";
import Container from "@mui/material/Container";
import { createTheme, ThemeProvider } from "@mui/material/styles";
import logo from "../../../../assets/SucuLove-logos/SucuLove-logos_transparent.png";
import { Link, useNavigate } from "react-router-dom";
import { useFormik} from 'formik'
import * as Yup from 'yup'
import {auth, db} from '../../../../firebaseConfig'
import { createUserWithEmailAndPassword } from "firebase/auth";
import { doc, setDoc} from 'firebase/firestore'

const theme = createTheme();

const SignUp = () => {

  const navigate = useNavigate()

  async function registrarUsuario(email, password, rol) {
    const infoUsuario = await createUserWithEmailAndPassword(
      auth,
      email,
      password
    ).then((usuarioFirebase) => {
      return usuarioFirebase;
    });
    console.log(infoUsuario.user.uid);
    const docRef = doc(db,`users/${infoUsuario.user.uid}`)
    setDoc(docRef, {email, rol})
  }

  const {handleSubmit, handleChange, errors, values} = useFormik({
    initialValues: {
      firstName:'',
      lastName: '',
      email:'',
      password:'',
    },
    onSubmit: (data) => {
      registrarUsuario(data.email, data.password, 'user');
      navigate('/products')
    },
    validationSchema: Yup.object().shape({
      firstName: Yup.string().required("Este campo es obligatorio").min(3, "Debe escribir un nombre"),
      lastName: Yup.string().required("Este campo es obligatorio").min(3, "Debe escribir un apellido"),
      email: Yup.string().required("Este campo es obligatorio").email("Debe ser formato email"),
      password: Yup.string().required("Este campo es obligatorio").min(6, "La contrase√±a debe tener minimo 6 caracteres "),
    }),
    validateOnChange: false,
})

  return (
    <ThemeProvider theme={theme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
          }}
        >
          <img src={logo} style={{ width: "50%" }}></img>
          <Typography component="h1" variant="h5">
            Sign up
          </Typography>
          <Box
            component="form"
            noValidate
            onSubmit={handleSubmit}
            sx={{ mt: 3 }}
          >
            <Grid container spacing={2}>
              <Grid item xs={12} sm={6}>
                <TextField
                  autoComplete="given-name"
                  name="firstName"
                  fullWidth
                  id="firstName"
                  label="First Name"
                  onChange={handleChange}
              error={errors.firstName ? true : false}
              helperText={errors.firstName}
              value={values.firstName}
                  autoFocus
                />
              </Grid>
              <Grid item xs={12} sm={6}>
                <TextField
                  fullWidth
                  id="lastName"
                  label="Last Name"
                  name="lastName"
                  autoComplete="family-name"
                  onChange={handleChange}
              error={errors.lastName ? true : false}
              helperText={errors.lastName}
              value={values.lastName}
                />
              </Grid>
              <Grid item xs={12}>
                <TextField
                  fullWidth
                  id="email"
                  label="Email Address"
                  name="email"
                  autoComplete="email"
                  onChange={handleChange}
              error={errors.email ? true : false}
              helperText={errors.email}
              value={values.email}
                />
              </Grid>
              <Grid item xs={12}>
                <TextField
                  required
                  fullWidth
                  name="password"
                  label="Password"
                  type="password"
                  id="password"
                  autoComplete="new-password"
                  onChange={handleChange}
              error={errors.password ? true : false}
              helperText={errors.password}
              value={values.password}
                />
              </Grid>
            </Grid>
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign Up
            </Button>
            <Grid container justifyContent="flex-end">
              <Grid item>
                <Link to="/sign-in" variant="body2">
                  Already have an account? Sign in
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
      </Container>
    </ThemeProvider>
  );
};

export default SignUp;
